
package desdecero01;

public class DesdeCero01 {
    
    public static void main(String[] args) {
        int n = 2;
        double A = 3.3;
        char C = 'H';
        System.out.println("Mostrar variables: " + n + ", " + A + ", " + C);
        System.out.println("Suma de n y A: " + (A + ((double)n)));
        System.out.println("Suma de n y A: " + (A - ((double)n)));
        System.out.println("C: " + (int)C);
    }
    
}
