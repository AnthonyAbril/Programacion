
package desdecero02;

public class DesdeCero02 {

    public static void main(String[] args) {
        int X = 3, Y = 4;
        double N = 2.2, M = 1.3;
        
        System.out.println("Primera operacion: " + (M * ((double)X)));
        System.out.println("Segunda operacion: " + (N + ((double)X)));
        System.out.println("Tercera operacion: " + (M - ((double)Y)));
        System.out.println("Cuarta operacion: " + (N + ((double)Y) - M));
    }
    
}
